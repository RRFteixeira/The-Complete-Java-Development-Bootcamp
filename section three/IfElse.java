

public class IfElse {

    public static void main(String[] args) {
        

        int grade=92;

        if (grade>=80)   {
            System.out.println("Great job, you got an A");

        }else if (grade>=70)   {
            System.out.println("Great job, you got an B");

        }else if (grade>=50)   {
            System.out.println("Great job, you got an C");

        }else{
            System.out.println("Study harder.");
        }


    }


}